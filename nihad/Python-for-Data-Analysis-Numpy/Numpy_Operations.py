import numpy as np

#Arithmetic Functions
arr = np.arange(0,10)

print(arr + arr)

print(arr * arr)

print(arr - arr)

print(arr/arr)

print(1/arr)

print(arr**3)

#Universal Array Functions
#Taking Square Roots
print(np.sqrt(arr))

#Calcualting exponential (e^)
print(np.exp(arr))

print(np.max(arr))

print(np.sin(arr))

print(np.log(arr))
